    <h3><strong>L'environnement du management international</strong></h3>
    <div class="w3-justify">
      <p>Les organisations opèrent sur un terrain de jeu global. L’environnement mondial est turbulent et se caractérise par ses multiples dimensions.Cette complexité est étroitement liée à l’hétérogénéité des pays, notamment entre ceux émergents et ceux qui sont déjà développés. La question de la régionalisation peut également entrer en ligne de compte.De plus, la globalisation se caractérise par le nombre croissant et la diversités
des acteurs impliqués dans les activités internationales : des firmes multinationales (FMN) et des petites et moyennes entreprises (PME), des entreprises ayant plusieurs localisations géographiques (pays développés 
et pays émergents), des firmes publiques ou privées, ainsi que des parties prenantes dont l’influence est croissante. Les acteurs sont nombreux,avec des spécificités et des intérêts divers.</p>
      <h3><strong>L'environnement Mondial<strong><h3>
      <p>Les organisations agissent dans le contexte de la globalisation. Bien que ce terme soit répandu et galvaudé et qu’il renvoie à des réalités différentes, nous considérons ici la globalisation comme une concurrence caractérisée par un réseau de liens internationaux qui relient les pays, les institutions 
et les personnes dans une économie globale interdépendante (Dereski,2011). Dans un monde de plus en plus dépourvu de frontières, la concurrence globale s’accélère par les avancées technologiques et par la montée 
des économies émergentes comme la Chine et l’Inde. C’est un processus que Thomas Friedman définit comme « nivelant le terrain de jeu » entre 
les pays ou comme « l’aplatissement du monde »2. La globalisation se caractérise ainsi par la mise en place d’un réseau, l’existence de liens internationaux et la notion d’interdépendance. Autrement dit, la globalisation est un 
processus de croissance des interconnexions à travers le monde dans toutes les sphères d’activité. Ces interconnexions peuvent conduire à une intégration, à une homogénéité, voire à une uniformité mondiale ou, au contraire, 
les spécificités des pays peuvent subsister. Les frontières au sein et entre les organisations et les nations deviennent alors floues. Dans ce chapitre, et 
plus globalement dans cet ouvrage, nous allons nous intéresser à la façon dont la globalisation affecte les organisations. Nous allons voir quelles sont les dimensions qui caractérisent l’environnement international, avant d’exposer la variété des pays et de préciser les enjeux de la régionalisation.</p>
      <h3><strong>Caractéristiques de l'environnement mondial<strong><h3>
      <p> L’environnement varie selon les pays. Il peut être appréhendé à travers six dimensions (voir figure 1.1)</p>
      <p><img src= "images/pic1.jpg" alt="picture" style="width:60%"></p>
    <p>Nous pouvons décrire chacune de ces dimensions comme suit: L’environnement politique : il est fonction, par exemple, du critère de 
stabilité ou d’instabilité politique, de la place de l’État dans la sphère économique (interventionnisme, libéralisme) et du risque politique.
On peut définir le risque politique comme toute action gouvernementale ou tout événement politiquement motivé qui peut affecter la 
profitabilité à long terme ou la valeur d’une entreprise. Il peut prendre la forme de nationalisations ou d’expropriations, comme en Bolivie 
ou au Venezuela (cas de l’entreprise Cemex en 2008), mais englobe également le risque terroriste, dans des pays du Moyen-Orient ou 
d’Amérique du Sud par exemple (attentats, prises d’otage, etc.).</p>
<p><strong>L’environnement légal</strong> : il est constitué des lois spécifiques à chaque pays. Il concerne notamment la législation en matière d’impôts, 
de droit des contrats et de protection des droits de propriété. À ce niveau national s’ajoutent les lois internationales, à travers le rôle des 
institutions internationales comme la Banque mondiale, l’Organisation mondiale du commerce (OMC) et les différentes agences des Nations Unies. Il existe également des législations à l’échelle régionale, comme au sein de l’Union européenne ou du North American 
Free Trade Agreement (NAFTA) liant le Canada, les États-Unis et le Mexique.
</p>
<p><strong>L’environnement économique</strong> : il comprend les concepts de système 
économique, d’état de développement, de produit national brut (PNB), 
de stabilité économique, de politiques monétaires et fiscales etc., et 
inclut des éléments tels que les taux d’intérêt, l’inflation, le chômage et 
la croissance économique. Le risque économique concerne la capacité 
d’un pays à remplir ses obligations financières. Il s’agit du risque que 
le gouvernement modifie sa politique économique, d’un point de vue 
monétaire ou fiscal (par exemple en dévaluant sa monnaie), ou celle 
portant sur les investissements étrangers. Ce type d’action peut rendre 
une entreprise non profitable ou incapable de rapatrier ses bénéfices 
étrangers, ce qui entraîne un risque financier ou lié au taux d’intérêt </p>

<p><strong>L’environnement technologique</strong>  : il évolue avec le développement 
rapide des technologies de l’information et des communications. Les 
progrès en matière de télécommunications ont fourni des opportunités pour multiplier les interactions économiques entre des lieux 
très éloignés. Les développements technologiques et leurs effets sur le commerce mondial et les transactions économiques sont majeurs.
On étudie par exemple le niveau technologique du pays, la disponibilité des compétences techniques au niveau local et l’état des infrastructures. Le risque ici concerne le transfert technologique et le piratage, ainsi que les restrictions légales en matière de protection de la 
propriété technologique.</p>

<p><strong>L’environnement culturel</strong> : il est perceptible à travers, notamment, les 
effets de la culture nationale sur l’activité de l’entreprise. La culture 
nationale correspond à l’ensemble des valeurs qu’une société considère comme importantes et aux normes de comportements approuvés ou sanctionnés dans une société (Deresky, 2011). La culture 
nationale peut affecter la manière dont les managers motivent ou 
coordonnent leurs salariés et celle dont une entreprise mène ses 
affaires.</p>

<p><strong>L’environnement écologique et sociétal</strong>  : il concerne, par exemple, 
les effets du réchauffement climatique et de la pollution, les questions de protection de l’environnement, de traitement des déchets, 
de consommation d’énergie, etc. Il inclut également l’environnement 
naturel, en l’occurrence les disponibilités des ressources en eau et en 
matières premières et le risque de catastrophes naturelles. Ces éléments peuvent avoir une incidence sur l’activité économique mondiale.Ainsi, l’environnement mondial est complexe, interdépendant et 
dynamique. Les managers doivent appréhender l’ensemble de ces dimensions à différents niveaux :
<ul><li>au niveau global, compte tenu de la localisation de la FMN partout dans le monde;</li>
    <li>au niveau régional, à savoir dans la zone géographique dans laquelle se situe la FMN ;</li>
    <li>au niveau local du fait de la nécessaire prise en compte des particularités locales des pays où la FMN est implantée.</li></ul>
    Ces éléments vont jouer un rôle dans le choix de la localisation de 
l’entreprise. La question de l’avantage concurrentiel n’est par ailleurs pas 
négligeable.</p>

