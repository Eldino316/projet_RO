Le kit associe plusieurs méthodes pédagogiques dans ses six UF, notamment : présentations techniques, 
apports acquis par l’analyse, travail et discussions en groupe et développement de contenus entre participants, 
réflexion sur des exemples:</p>
<p><strong> les diaporamas </strong> sous tout format convenant à une présentation sur ordinateur (PowerPoint (PPT) 
ou autre logiciel) projetée sur écran. Elles comprennent des tableaux et des éléments visuels -graphes, 
diagrammes ou organigrammes - servant à transmettre le contenu et à fournir un support visuel. Afin 
