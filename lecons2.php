    <div class="w3-justify">
      <p>On apprend continuellement, partout et tout au long de la vie. Mais souvent 
l’acquisition de connaissances en dehors du système formel d’éducation et 
de formation n’est ni bien comprise ni valorisée.
La formation sur le tas, l’apprentissage informel, la gestion d’un ménage, les 
soins aux malades et aux parents âgés sont autant d’activités qui permettent 
d’accumuler des acquis d’apprentissages mais qui souvent ne sont pas 
assorties d’un certificat de compétences validant les savoirs, savoir-faire et 
l’expérience acquises. La reconnaissance de tous les types d’apprentissage 
peut se traduire par des avantages sur le marché du travail, dans l’éducation 
et la formation formelles, sur le plan financier et en termes d’estime de soi.
Partout dans le monde, la valeur de l’apprentissage non-formel et informel 
est reconnue de façon croissante et beaucoup de pays mettent en place 
des systèmes pour valoriser les compétences acquises par ces modes de 
formation. Dans un contexte où il est nécessaire de faciliter la transition de 
l’économie informelle vers l’économie formelle et devant l’augmentation des 
flux migratoires, l’OIT est de plus en plus souvent sollicitée pour aider les 
mandants à établir, évaluer et réviser les systèmes de validation des acquis 
de l’expérience (VAE) dans toutes les régions. Ce kit de formation repose 
sur cette expertise. Il décrit les principaux éléments d’un système de VAE 
performant et accessible à tous et montre par une multitude d’études de 
cas et d’exemples comment différents pays ont abordé cette entreprise. Il 
s’adresse à un public large et mixte, notamment les mandants de l’OIT, les 
hauts fonctionnaires, les organisations d’employeurs et de travailleurs, les 
évaluateurs et les formateurs.
</p>
      <h3><strong>Objectifs généraux du kit<strong><h3>
      <p>Ce kit de formation sur la validation des acquis (VAE) s’adresse à un public large et varié, comprenant les 
mandants de l’OIT, les hauts fonctionnaires, les organisations d’employeurs et de travailleurs, le personnel 
du dispositif des Nations unies (ONU) et le personnel d’autres organisations multilatérales ou régionales, les 
agences bilatérales, certaines organisations non gouvernementales, les services publics chargés de l’emploi et 
les agences privées pour l’emploi, les dirigeants des établissements d’éducation et de formation et les cadres, 
évaluateurs et animateurs concernés par la VAE.
La structure modulaire (composée de six unités de formation) permet de mieux répondre aux besoins de 
différents groupes de participants. Les unités peuvent également être utilisées individuellement, en fonction 
du groupe de participants.
Ce kit de formation propose des informations, des idées et des connaissances au travers d’exemples concrets 
de pays en développement et de pays développés. Il s’intéresse tant aux hommes qu’aux femmes et aux 
groupes défavorisés.</p>
